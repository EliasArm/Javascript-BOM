/*
    Navigator
    Metodos para trabajar con el navegador
*/
// Mostrar si tiene activadas las Cookies
console.log(' --- navigator.cookieEnabled --- ');
console.log('Cookies activadas: ', navigator.cookieEnabled);

// Conocer el vavegador Web, Sistema Operativo, etc 
console.log(' --- navigator.userAgent --- ');
console.log(navigator.userAgent);

// Conocer el lenguaje del navegador
console.log(' --- navigator.languaje --- ');
console.log(navigator.language);

// Conocer si el navegador esta conectado a Internet
console.log(' --- navigator.onLine --- ');
console.log(navigator.onLine)